public class FloatingPointPrecisionChallenge {
    public static void main(String[] args) {
        double poundToKilo = 0.45359237;
        double myPound = 100d;
        double myKilograms = myPound * poundToKilo ;
        System.out.println("100 pounds are equivalent to " + myKilograms + " kilograms ");

    }
}